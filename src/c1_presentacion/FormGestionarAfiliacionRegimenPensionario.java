    private void llenaComboTipoDocumento(javax.swing.JComboBox jComboBox) {      
        DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
        jComboBox.removeAllItems();

        GestionarTipoDocumentoServicio gestionarTipoDocumentoServicio = new GestionarTipoDocumentoServicio();
        try {
            tipodocumentos = gestionarTipoDocumentoServicio.cargarTipoDocumento();
        
            for(TipoDocumento tipodocumento : tipodocumentos){
                modeloCombo.addElement(new Banda(tipodocumento.getDescripcion(),tipodocumento.getTipodocumentoid()));
            }
            jComboBox.setModel(modeloCombo);
            
        } catch (Exception e) {
            Mensaje.mostrarErrorDeConsulta(this);
        }
    }
    
    private void llenaComboRegimenPensionario(javax.swing.JComboBox jComboBox) {      
        DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
        jComboBox.removeAllItems();

        GestionarRegimenPensionarioServicio gestionarRegimenPensionarioServicio = new GestionarRegimenPensionarioServicio();
        try {
            regimenPensionarios = gestionarRegimenPensionarioServicio.cargarRegimenPensionario();
        
            for(RegimenPensionario regimenPensionario : regimenPensionarios){
                modeloCombo.addElement(new Banda(regimenPensionario.getDescripcion(),regimenPensionario.getRegimenpensionarioid()));
            }
            jComboBox.setModel(modeloCombo);
            
        } catch (Exception e) {
            Mensaje.mostrarErrorDeConsulta(this);
        }
    }
