    private void botBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botBuscarActionPerformed
        // TODO add your handling code here:
        try {
            GestionarEmpleadoServicio gestionarEmpleadoServicio = new GestionarEmpleadoServicio();
                    
            empleado = gestionarEmpleadoServicio.buscarEmpleado(Funciones.codIntCombo(this.jCboTipDocu), this.jTxtNumero.getText());
            if(empleado != null)
                this.jLblEmpleado.setText(empleado.getApellidopaterno() + " " + empleado.getApellidomaterno() + ", " + empleado.getNombre());
            else
                Mensaje.mostrarAdvertencia(this, "El empleado no se encuentra registrado");
                activarBotonBuscar();
        } catch (Exception e) {
            Mensaje.mostrarErrorDeConsulta(this);
        }
        
    }//GEN-LAST:event_botBuscarActionPerformed

    private void botGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botGrabarActionPerformed
        // TODO add your handling code here:
        int registros_afectados;
        try {
            afiliacionRegimenPensionario.setEmpleado(empleado); //empleado ya lo tengo desde la busqueda.
            afiliacionRegimenPensionario.setFechaafiliacion(this.jDtChsFecAfi.getDate());
            afiliacionRegimenPensionario.setNumerocuspp(this.jTxtCussp.getText());
            
            GestionarRegimenPensionarioServicio gestionarRegimenPensionarioServicio =  new GestionarRegimenPensionarioServicio();
            regimenPensionario = gestionarRegimenPensionarioServicio.buscarRegimenPensionario(Funciones.codIntCombo(this.jCboRegimenPensionario));
            afiliacionRegimenPensionario.setRegimenpensionario(regimenPensionario);
            
            afiliacionRegimenPensionario.setTipocomision((String) this.jCboTipoRegimen.getSelectedItem());
            
            GestionarAfiliacionRegimenPensionarioServicio gestionarAfiliacionRegimenPensionarioServicio = new GestionarAfiliacionRegimenPensionarioServicio();
            
            if (empleado.tieneAsignacionFamiliar()==true)
                JOptionPane.showMessageDialog(this,"Empleado ya esta Afiliado.","EV-Soft: Advertencia",JOptionPane.WARNING_MESSAGE);
            else{
                registros_afectados = gestionarAfiliacionRegimenPensionarioServicio.crearAfiliacionRegimenPensionario(afiliacionRegimenPensionario);

                if (registros_afectados==1){
                    Mensaje.mostrarAfirmacionDeCreacion(this);
                    //inicializarEmpleado();
                }
                else
                    Mensaje.mostrarAdvertenciaDeCreacion(this);
            }
        } catch (Exception e) {
            Mensaje.mostrarError(this, e.getMessage());
        }
    }//GEN-LAST:event_botGrabarActionPerformed


